/* 
Floating point literals are pretty easy to understand

floating point values are stored as double unless they are suffixed with an F
*/

// Examples:
double d = 8.31;
double e = 3.00e+8;
float f = 8.31F;
float g = 3.00e+8F;
// Using the same formatting as the int underscore notation (can only be done AFTER the decimal)
float pi = 3.14_159_265_358;
